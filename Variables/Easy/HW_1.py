# Variables - Easy - 1 - Math Operation

num1 = float(input("Enter First number: "))
num2 = float(input("Enter First number: "))
print(str(num1) + ' + ' + str(num2) + ' = ' + str(num1+num2))
print(str(num1) + ' - ' + str(num2) + ' = ' + str(num1-num2))
print(str(num1) + ' * ' + str(num2) + ' = ' + str(num1*num2))
print(str(num1) + ' / ' + str(num2) + ' = ' + str(num1/num2))
print("End of Program")


